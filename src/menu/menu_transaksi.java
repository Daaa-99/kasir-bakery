    private void btn_inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inputActionPerformed
        // TODO add your handling code here:
        try {
            transaksi tran = new transaksi();   
            text_totalbayar.setText("" + tran.total_bayar);
            this.stat = k.getCon().prepareStatement("INSERT INTO transaksi (id_transaksi, nama_pelanggan, id_produk, tanggal,  harga, jumlah_beli, total_bayar, Id_user) VALUES (?, ?, ?, ?, ?, ?, ?, ?)");
            this.stat.setInt(1, 0); // Kolom id_transaksi akan diisi otomatis oleh database
            this.stat.setString(2, tran.nama_pelanggan); // nama_pelanggan
            this.stat.setInt(3, tran.id_produk);         // id_produk
            this.stat.setString(4, tran.tanggal);       // tanggal
            this.stat.setInt(5, tran.harga);            // harga
            this.stat.setInt(6, tran.jumlah_beli);      // jumlah_beli
            this.stat.setInt(7, tran.total_bayar);      // total_bayar
            this.stat.setInt(8, Session.Id_user);          // id_user
            int pilihan = JOptionPane.showConfirmDialog(null,
                    "Tanggal: "+tran.tanggal+
                    "\nNama Pelanggan: "+tran.nama_pelanggan+
                    "\nPembelian: "+tran.jumlah_beli+" "+tran.nama_produk+
                    "\nTotal Bayar "+tran.total_bayar+"\n",      
                    "Tambahkan Transaksi?",
                    JOptionPane.YES_NO_OPTION);
            if (pilihan == JOptionPane.YES_OPTION) {
                this.stat.executeUpdate();
                refreshTable();
            } else if(pilihan == JOptionPane.NO_OPTION){
                refreshTable();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
         }
    }//GEN-LAST:event_btn_inputActionPerformed

