    class transaksi extends menu_transaksi{
        int id_transaksi, id_produk, harga, jumlah_beli, total_bayar, Id_user;
        String nama_pelanggan, tanggal, nama_produk;
        
        
        public transaksi(){
            this.Id_user = Session.Id_user;;
            this.nama_pelanggan = text_namapelanggan.getText();
            String combo = combo_idproduk.getSelectedItem().toString();
            String[]arr = combo.split(":");
            this.id_produk = Integer.parseInt(arr[0]);
            try {
                Date date = text_tanggal.getDate();
                DateFormat dateformat = new SimpleDateFormat("yyyy-MM-dd");
                this.tanggal = dateformat.format(date);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            this.nama_produk = arr[1];
            this.harga = Integer.parseInt(arr[2]);
            this.jumlah_beli = Integer.parseInt(text_jumlahbeli.getText());
            this.total_bayar = this.harga * this.jumlah_beli;       
        }
    }
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Transaksi");
        model.addColumn("Nama Pelanggani");
        model.addColumn("ID Produk");
        model.addColumn("Tanggal");
        model.addColumn("Nama Produk");
        model.addColumn("Harga");
        model.addColumn("Jumlah Beli");
        model.addColumn("Total Bayar");
        model.addColumn("Kasir");
        table_transaksi.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select transaksi.*, produk.nama_produk, user.Nama as nama_kasir from transaksi "
                    + "JOIN user ON transaksi.Id_user=user.id_user "
                    + "JOIN produk ON transaksi.id_produk=produk.id_produk");
            this.rs = this.stat.executeQuery();
            while(rs.next()){
                Object[] data = {
                  rs.getInt("id_transaksi"),
                  rs.getString("nama_pelanggan"),
                  rs.getInt("id_produk"),
                  rs.getString("tanggal"),
                  rs.getString("nama_produk"),
                  rs.getInt("harga"),
                  rs.getInt("jumlah_beli"),
                  rs.getInt("total_bayar"),
                  rs.getString("nama_kasir")
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_idtransaksi.setText("");
        text_namapelanggan.setText("");
        text_jumlahbeli.setText("");
        text_totalbayar.setText("");
    } 
    
    public void refreshCombo(){
        try {
            this.stat = k.getCon().prepareStatement("select * from produk "
                    + "where status = 'Tersedia'");
            this.rs = this.stat.executeQuery();
            while(rs.next()){
                combo_idproduk.addItem(rs.getString("id_produk")+":"+
                rs.getString("nama_produk")+":"+rs.getString("harga"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
   
    
    
    
