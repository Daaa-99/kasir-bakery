    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        try {
            produk p = new produk();
            this.stat = k.getCon().prepareStatement("update produk set nama_produk = ?,"
                    + "harga = ?,  status = ? where id_produk = ?");
            stat.setString(1, p.nama_produk);
            stat.setInt(2, p.harga);
            stat.setString(3, p.status);
            stat.setInt(4, Integer.parseInt(text_idproduk.getText()));
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_updateActionPerformed

