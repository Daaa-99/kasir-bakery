    class produk extends menu_produk{
        int id_produk, harga;
        String nama_produk, status;
        
        public produk(){
            this.nama_produk = text_namaproduk.getText();
            this.harga = Integer.parseInt(text_hargaproduk.getText());
            this.status = combo_statusproduk.getSelectedItem().toString();
        }
    }
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Produk");
        model.addColumn("Nama Produk");
        model.addColumn("Harga");
        model.addColumn("Status Produk");
        table_produk.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from produk");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {                
                Object[] data ={
                    rs.getInt("id_produk"),
                    rs.getString("nama_produk"),
                    rs.getInt("harga"),
                    rs.getString("status")
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_idproduk.setText("");
        text_namaproduk.setText("");
        text_hargaproduk.setText("");
    }

